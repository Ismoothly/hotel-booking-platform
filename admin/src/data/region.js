/**
 * 根据已选省市区路径反解析为 Cascader 的 value 数组
 * city 可能为 "北京" 或 "北京市"，address 可能包含区县
 */
export function getRegionValueFromHotel(hotel) {
  if (!hotel) return undefined;
  const cityStr = (hotel.city || '').trim();
  const addressStr = (hotel.address || '').trim();
  if (!cityStr) return undefined;
  const normalize = (s) => (s || '').replace(/[省市]/g, '');
  const cityNorm = normalize(cityStr);
  for (let i = 0; i < regionData.length; i++) {
    const prov = regionData[i];
    for (let j = 0; j < (prov.children || []).length; j++) {
      const city = prov.children[j];
      const cityName = city.value;
      const match =
        cityName === cityStr ||
        cityStr === cityName ||
        cityName.includes(cityStr) ||
        cityStr.includes(cityName) ||
        (cityNorm && (normalize(cityName) === cityNorm || cityName.includes(cityStr)));
      if (match) {
        const districts = city.children || [];
        let districtValue = null;
        for (let k = 0; k < districts.length; k++) {
          if (addressStr.includes(districts[k].value)) {
            districtValue = districts[k].value;
            break;
          }
        }
        if (districtValue) return [prov.value, cityName, districtValue];
        return [prov.value, cityName];
      }
    }
  }
  return undefined;
}
